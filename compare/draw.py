# =========================================================
# 误差 → 准确率
# =========================================================
def error_to_accuracy(data_dict):
    return {k: [1 - x for x in v] for k, v in data_dict.items()}

distance_acc_snr = error_to_accuracy(distance_errors_snr)
power_acc_snr = error_to_accuracy(power_errors_snr)
distance_acc_height = error_to_accuracy(distance_errors_height)
power_acc_height = error_to_accuracy(power_errors_height)

# =========================================================
# 绘图函数（优化版）
# =========================================================
def plot_curve(x, data, xlabel, ylabel, filename):
    plt.figure(figsize=(8, 5))

    for m in METHODS:
        style = style_map[m]
        plt.plot(
            x,
            data[m],
            label=m.replace("_", "-"),
            color=style["color"],
            marker=style["marker"],
            linestyle=style["linestyle"],
        )

    plt.xlabel(xlabel)
    plt.ylabel(ylabel)
    plt.grid(True, linestyle="--", alpha=0.5)

    # 坐标轴加粗
    for spine in plt.gca().spines.values():
        spine.set_linewidth(1.2)

    plt.legend(
        ncol=2,
        frameon=True,
        fontsize=16,  # ✅ 字体变大（核心）
        markerscale=1.8,  # ✅ 图例里的点也变大
        handlelength=2.5,  # ✅ 线更长更清晰
        columnspacing=1.5,  # ✅ 两列间距更舒服
    )
    plt.tight_layout()
    plt.savefig(filename, dpi=600)
    plt.close()

